            <br/><b>FRONTEND:</b> It refers to the static webpages(HTML Pages), you designed in Adobe Dreamweaver
            (or) through programming.<br/><br/>
            <b>BACKEND:</b> It refers to data-storage, data-driving and adding functional logic.It composed into four parts:<br/>
            a) Databases<br/>b) Data Access Layers(DAL)<br/>c) Data Access Controllers(DAC)<br/>d) Javascript<br/>
            <b>a) Databases:</b> Data storage is done in Databases in tables format(Row, column Structure). WebDynamics Version 1.0 is
            concentrated on MySQL to access and manipulate in MySQL Server.<br/>
            <b>b) Data Access Layers(DAL):</b>It is a PHP programming Layer.It read outs the structure of the tables in the databases
            and creates possible queries.<br/>
            <b>c) Data Access Controllers(DAC):</b>It is also a PHP programming Layer.In this layer, functional logics, the
            front-end and backend linking logics are created and the work of data-driving to database is done.<br/>
            <b>d) Javascript:</b>It is a scripting language layer which communicates between HTML and PHP Programming Language.
